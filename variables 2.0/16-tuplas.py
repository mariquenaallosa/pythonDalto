# Crear tuplas con tuple()
tupla = tuple(["dato1","dato2"])
print(tupla)

# Crear tupla sin () -> equivalente a crear tupla con ()
tupla2 = "dato1", "dato2"
print(tupla2)



# Crear tupla de un sólo dato 
tupla3 = "dato1",
print(tupla3)


